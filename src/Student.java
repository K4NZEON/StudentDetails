
public class Student {
    private String name;
    private int age;
    
    // Getters
    
    public String getName(){
        return this.name;
    }
    
    public int getAge(){
        return this.age;
    }
    
    // Setters
    
    public void setName(String newName){
        this.name = newName;
    }
    
    public void setAge(int newAge){
        this.age = newAge;
    }
}
